public class FindHightestNum {

	public static void main(String[] args) {
		int x=400;
		int y=200;
		int z=300;
		
		if((x>y) && (x>z)) {
			System.out.println("x is the greatest");
		}
		else if((y>x) && (y>z)) {
			System.out.println("y is the greatest");
		}
		else {
			System.out.println("z is the greatest");
		}
   
	}

}
